export function getStateLabel(country: string) {
  switch (country) {
    case 'US':
      return 'State';
    case 'CA':
      return 'Province';
    case 'MX':
      return 'State';
    case 'GB':
      return 'Region';
    case 'AU':
      return 'State/Territory';
    case 'DE':
      return 'State';
    case 'INTL':
      return 'State/Province/Region';
    default:
      return 'State/Province/Region';
  }
}

